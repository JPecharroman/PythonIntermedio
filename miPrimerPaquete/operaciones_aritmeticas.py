# Creamos este archivo para definir algunas operaciones aritmeticas basicas que estaran dentro de nuestro primer paquete

def suma_dos_numeros(num1: int, num2: int) -> int:
    return num1 + num2

def resta_dos_numeros(num1: int, num2: int) -> int:
    return num1 - num2

def multiplicar_dos_numeros(num1: int, num2: int) -> int:
    return num1 * num2

def dividir_dos_numeros(num1: int, num2: int) -> float:
    return num1 / num2
